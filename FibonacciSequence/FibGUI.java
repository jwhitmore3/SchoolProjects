import javax.swing.JOptionPane;
//Simple GUI for taking user input

public class FibGUI{
	public static void main(String[] args)
	{
		int fibNumber;
		String fibNumberS;
		//prompt user for first name
		fibNumberS = JOptionPane.showInputDialog("Enter nTh Fibonacci number:");
		fibNumber = Integer.parseInt(fibNumberS);
		long result = FibList.fibList(fibNumber);
		String message = "The "+ fibNumber+"th number in the fibonacci sequence is: " + result + "\n Another Educational App Brought to you by Joshua Whitmore"; 
		JOptionPane.showMessageDialog(null, message);
		System.exit(0);
	}
}
